package org.builder;

public class House {
	private String basic;
	private String wall;
	private String roff;

	public String getBasic() {
		return basic;
	}
	public void setBasic(String basic) {
		this.basic = basic;
	}
	public String getWall() {
		return wall;
	}
	public void setWall(String wall) {
		this.wall = wall;
	}
	public String getRoff() {
		return roff;
	}
	public void setRoff(String roff) {
		this.roff = roff;
	}
}
